package br.edu.fa7.projeto04;

public class Cidade {
	
	private String nome;
	private int imagem;
	
	public Cidade() {
		
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getImagem() {
		return imagem;
	}

	public void setImagem(int imagem) {
		this.imagem = imagem;
	}
	
}